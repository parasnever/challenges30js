// Feature Request 1: Write a script that declares variables of different data types and logs both the value and type of each variable to the console.
let num = 123;
let str = "Hello, JavaScript";
let bool = true;
let obj = { name: "Alice", age: 25 };
let arr = ["apple", "banana", "cherry"];

console.log(`Value: ${num}, Type: ${typeof num}`);  // Output: Value: 123, Type: number
console.log(`Value: ${str}, Type: ${typeof str}`);  // Output: Value: Hello, JavaScript, Type: string
console.log(`Value: ${bool}, Type: ${typeof bool}`); // Output: Value: true, Type: boolean
console.log(`Value: ${obj}, Type: ${typeof obj}`);  // Output: Value: [object Object], Type: object
console.log(`Value: ${arr}, Type: ${typeof arr}`);  // Output: Value: apple,banana,cherry, Type: object
